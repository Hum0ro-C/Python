"""
题目：百钱百鸡
含义：公鸡5文钱一只，母鸡3文钱一只，小鸡1文钱三只。
      现在用100文钱买100只鸡，问：公鸡、母鸡、小鸡各买几只？   

version：1.0.0
Author: Hum0ro

"""

for x in range(0,21):
    for y in range(0,34):
        z = 100 - x - y
        if z % 3 == 0:
            if 5 * x + 3 * y + z // 3 == 100:
                print(f"公鸡：{x} 只，母鸡：{y} 只，小鸡：{z} 只")
